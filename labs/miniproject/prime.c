#include <stdio.h>
#include <stdlib.h>



int main(){
    int i,j,limit;
    int primes[100];
    int z = 1;
    printf("Enter the Number ");
    scanf("%d",&limit);
	

    for (i=2;i<limit;i++)
        primes.append(1);

    for (i=2;i<limit;i++)
        if (primes[i])
            for (j=i;i*j<limit;j++)
                primes[i*j]=0;

    for (i=2;i<limit;i++)
        if (primes[i])
            printf("%dth prime = %d\n",z++,i);

return 0;
}
